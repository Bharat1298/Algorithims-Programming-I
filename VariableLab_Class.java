
public class VariableLab_Class {

	public static void main(String[] args) {
		int number = 9;
		byte var = 20;
		short negative = -30234;
		long big = 23423232343L;
		float deci = 3.14159265F;
		char letter = 'a';
		boolean valid = true;
		
		System.out.println("number: " + number);
		System.out.println("var: " + var);
		System.out.println("negative: " + negative);
		System.out.println("big: " + big);
		System.out.println("deci: " + deci);
		System.out.println("letter: " + letter);
		System.out.println("valid: " + valid);
		
		System.out.println();
		
		System.out.println("VARIABLE CHANGE");
		
		System.out.println();
		
		number = 1;
		var = 40;
		negative = -26142;
		big = 349023942333L;
		deci = 2.7182818284F;
		letter = 'z';
		valid = false;
		
		System.out.println("number: " + number);
		System.out.println("var: " + var);
		System.out.println("negative: " + negative);
		System.out.println("big: " + big);
		System.out.println("deci: " + deci);
		System.out.println("letter: " + letter);
		System.out.println("valid: " + valid);
		
	}

}
